-- Creación de esquema.
CREATE SCHEMA everis_fpdual_salesianasnervion DEFAULT CHARACTER SET utf8;

USE everis_fpdual_salesianasnervion;

-- CREACIÓN DE TABLAS.
-- CREACIÓN TABLA PROFESOR.
CREATE TABLE Profesor (
	
	nif varchar(9) NOT NULL,
	nombre VARCHAR(35),
	salario double,
	
	PRIMARY KEY(nif)
);

-- CREACIÓN TABLA ASIGNATURA.
CREATE TABLE Asignatura (
	
	id_asig INT NOT NULL,
	nombre VARCHAR(35),
	
	PRIMARY KEY(id_asig)
);

-- CREACIÓN TABLA ASIG-PROF.
-- Tabla creado por la necesidad de una relación N-M.
CREATE TABLE RelacionAsigProf (
	
	id_prof varchar(9) NOT NULL,
	id_asig int not null,
	
	FOREIGN KEY(id_prof) REFERENCES Profesor(nif),
	FOREIGN KEY(id_asig) REFERENCES Asignatura(id_asig)
);

-- CREACIÓN TABLA MATRICULA.
create table Matricula(

	n_matricula int not null,
	fecha_matricula date,
	modalidad varchar(45),
	
	primary key(n_matricula)

);

-- CREACIÓN TABLA CENTRO.
create table Centro(

	id_centro int not null,
	localidad varchar(45),
	nombre varchar(45),
	
	primary key(id_centro)

);

-- CREACIÓN TABLA ALUMNO.
create table Alumno (

	nif varchar(9) NOT NULL,
	nombre varchar(45),
	id_centro int not null,
	n_matricula int not null,
	edad int,
	
	primary key(nif),
	FOREIGN KEY(id_centro) REFERENCES Centro(id_centro),
	FOREIGN KEY(n_matricula) REFERENCES Matricula(n_matricula)
);

-- CREACIÓN TABLA NOTAS.
-- Relación creada por la necesidad de relaciones N-M.
create table Notas (

	id_alumno varchar(9) not null,
	id_asig int not null,
	nota int,
	
	FOREIGN KEY(id_asig) REFERENCES Asignatura(id_asig),
	FOREIGN KEY(id_alumno) REFERENCES Alumno(nif)

);

